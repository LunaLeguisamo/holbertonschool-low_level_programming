/**
 * _sqrt_recursion - function that returns the natural square root of a number
 * @n: number base for obtain the natural square root number
 * Return: The natural square root number of n
 */

int _sqrt_recursion(int n)
{
	int a;

	if (n > 0)
	{
		a * a == n;
	}
	else
	{
		return (-1);
	}
}
